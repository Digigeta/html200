// Declare four variables
let a = 10;
let b = 5;
let c = 20;
let d = 2; 

// Addition
let additionResult = a + b;
console.log('Addition result (a + b):', additionResult); // 15

// Subtraction
let subtractionResult = c - a;
console.log('Subtraction result (c - a):', subtractionResult); // 10

// Multiplication
let multiplicationResult = b * d;
console.log('Multiplication result (b * d):', multiplicationResult); // 10

// Division
let divisionResult = c / b;
console.log('Division result (c / b):', divisionResult); // 4

// Modulus (Remainder)
let modulusResult = c % d;
console.log('Modulus result (c % d):', modulusResult); // 0

// Exponentiation
let exponentiationResult = b ** d;
console.log('Exponentiation result (b ** d):', exponentiationResult); // 25

// Mixed operation
let mixedResult = (a + b) * (c / d) - b;
console.log('Mixed operation result ((a + b) * (c / d) - b):', mixedResult); // 85

// Increment and Decrement
let incrementResult = ++a; // a is now 11
console.log('Increment result (++a):', incrementResult); // 11

let decrementResult = --b; // b is now 4
console.log('Decrement result (--b):', decrementResult); // 4
